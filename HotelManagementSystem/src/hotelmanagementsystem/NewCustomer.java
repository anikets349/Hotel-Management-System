    public boolean validateID(String IDType,String ID){
        if(IDType=="Aadhar"){
            if(ID.length()!=12){
                JOptionPane.showMessageDialog(this,"Aadhar number must be of 12 digits");
                idField.setText("");
                return false;
            }
        }
        else if(IDType=="PAN"){
            if(ID.length()!=10){
                JOptionPane.showMessageDialog(this,"PAN number must be of 10 digits");
                idField.setText("");
                return false;
            }
        }
        else if(IDType=="Passport"){
            if(ID.length()!=7){
                JOptionPane.showMessageDialog(this,"Passport number must be of 10 digits");
                idField.setText("");
                return false;
            }
        }
        return true;
    }

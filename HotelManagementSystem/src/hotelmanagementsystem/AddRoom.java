    private void AddRoomBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddRoomBtnActionPerformed
        // TODO add your handling code here:
        System.out.println("Handler");
        int roomNo = Integer.parseInt(RoomTF.getText());
        char isAvailable='N';
        if(AvailYes.isSelected()){
            isAvailable = 'Y';
        }
        String cleaningStatus = (String)ClStatusCombo.getSelectedItem();
        Float price = Float.parseFloat(PriceTF.getText());
        String bedType = (String)BedTypeCombo.getSelectedItem();
        System.out.println(roomNo+" "+isAvailable+" "+cleaningStatus+" "+price+" "+bedType);
        Conn con = new Conn();
        String query = "insert into c##aniket.ROOMC values("+roomNo+",'"+isAvailable+"','"+cleaningStatus+"',"+price+",'"+bedType+"')";
        try{
            con.stm.executeQuery(query);
            JOptionPane.showMessageDialog(this,"New Room Added");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_AddRoomBtnActionPerformed


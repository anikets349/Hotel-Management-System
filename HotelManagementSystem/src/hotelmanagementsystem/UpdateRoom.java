    private void loadDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadDetailsActionPerformed
        // TODO add your handling code here:
        String id = (String)custID.getSelectedItem();
        String query = "select * from c##aniket.CUSTOMERC where ID_NO='"+id+"'";
        int roomNum = 0;
        try{
            Conn con = new Conn();
            ResultSet rs = con.stm.executeQuery(query);
            while(rs.next()){
                roomNo.setText(rs.getString("RoomNo"));
                roomNum = Integer.parseInt(roomNo.getText());
            }
            String query1 = "select * from c##aniket.ROOMC where ROOM_NO="+roomNum;
            ResultSet rs1 = con.stm.executeQuery(query1);
            while(rs1.next()){
                avl.setText(rs1.getString("Availability"));
                cleanStatus.setText(rs1.getString("Cleaning_Status"));                
            }
            avlCombo.setEnabled(true);
            clStatusCombo.setEnabled(true);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error occured while fetching details");
        }
    }//GEN-LAST:event_loadDetailsActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        boolean flag = false;
        int rNo = Integer.parseInt(roomNo.getText());
        String isAvail = (String)avlCombo.getSelectedItem();
        String clStatus = (String)clStatusCombo.getSelectedItem();
        if(!isAvail.equals(avl.getText())){
            String query = "update c##aniket.ROOMC set AVAILABILITY='"+isAvail+"' where ROOM_NO="+rNo;
            try{
                Conn con = new Conn();
                con.stm.executeQuery(query);
                flag = true;
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(this, "Cannot update staus");
            }
        }
        if(!clStatus.equals(cleanStatus.getText())){
            String query = "update c##aniket.ROOMC set CLEANING_STATUS='"+clStatus+"' where ROOM_NO="+rNo;
            try{
                Conn con = new Conn();
                con.stm.executeQuery(query);
                flag = true;
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(this, "Cannot update staus");
            }
        }
        if(flag){
            JOptionPane.showMessageDialog(this, "Room Status updated");
        }
    }//GEN-LAST:event_updateActionPerformed

